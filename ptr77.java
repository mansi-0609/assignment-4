class Ptr77 {
    public static void main(String args[]) {
        for (int i = 1; i <= 4; i++) {
             System.out.print(1);
            for (int j = 1; j < i; j++) {
                System.out.print(" ");
            }
            System.out.print(i);
            System.out.println();
        }
        for (int i = 3; i >= 1; i--) {
            System.out.print(1);
            for (int j = 1; j < i; j++) {
                System.out.print(" ");
            }
            System.out.print(i);
            System.out.println();
        }
    }
}
